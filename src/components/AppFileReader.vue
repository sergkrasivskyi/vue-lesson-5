    <label class="file-loader" for="file">–í–∏–±–µ—Ä—ñ—Ç—å —Ç–µ–∫—Å—Ç–æ–≤–∏–π —Ñ–∞–π–ª (*.txt)
      <input id="file" type="file" @change="readFile" />
    </label>
</template>

<script>
  export default {
    name: "AppFileReader",
    methods: {
      readFile(event) {
        // –º–æ–∂–Ω–∞ –æ—Ç—Ä–∏–º—É–≤–∞—Ç–∏ –¥–æ—Å—Ç—É–ø –¥–æ –∑–∞–≤–∞–Ω—Ç–∞–∂–µ–Ω–æ–≥–æ —Ñ–∞–π–ª—É —á–µ—Ä–µ–∑
          //  ref='doc' —É —Ä–æ–∑–º—ñ—Ç—Ü—ñ
          // file = this.$refs.doc.files[0];
       // –ê –º–æ–∂–Ω–∞ –ø–µ—Ä–µ–¥–∞—Ç–∏ –ø–æ–¥—ñ—é —ñ –æ—Ç—Ä–∏–º–∞—Ç–∏ –¥–æ—Å—Ç—É–ø –¥–æ —Ñ–∞–π–ª—É —á–µ—Ä–µ–∑ –ø–æ–¥—ñ—é
          // file = event.target.files[0]
        
        const file = event.target.files[0]
        const reader = new FileReader();
        if (file.name.includes(".txt")) {
          reader.readAsText(file)
          reader.onload = (res) => this.$emit('filereaded', res.target.result)
        }
      }
    }
  }
</script>

